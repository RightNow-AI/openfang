//! # maestro-algorithm
//!
//! The 7-Phase Algorithm Pipeline ported from Maestro's PAI framework,
//! redesigned to integrate with OpenFang's workflow engine and Rig.rs
//! model abstraction layer.
//!
//! ## Phases
//!
//! 1. **OBSERVE** — Gather context, read environment, assess current state
//! 2. **ORIENT** — Analyze the task, decompose complexity, identify constraints
//! 3. **PLAN** — Generate execution plan with Ideal State Criteria (ISC)
//! 4. **EXECUTE** — Run the plan steps (delegates to OpenFang agents)
//! 5. **VERIFY** — Mechanically verify ISC satisfaction against output
//! 6. **LEARN** — Capture learnings, patterns, and failure modes
//! 7. **ADAPT** — Update algorithm parameters based on accumulated learnings
//!
//! ## Integration with OpenFang
//!
//! This crate does NOT replace OpenFang's `openfang-kernel::workflow`.
//! Instead, it provides a higher-level orchestration pattern that can be
//! registered as a workflow template. OpenFang's supervisor dispatches
//! agents; this crate defines WHAT those agents should do at each phase.
//!
//! ## HONEST GAPS (things that need work)
//!
//! - The ADAPT phase is conceptual only — no real parameter mutation yet
//! - ISC generation is LLM-dependent and can produce vague criteria
//! - No integration with OpenFang's task board for multi-agent EXECUTE
//! - Learning storage is local; needs OpenFang's memory system integration
//! - Retry logic is simplistic (fixed count, no exponential backoff)

