#[cfg(test)]
mod tests {
    use super::*;
    use crate::swe::{SWETestExpectedOutput, SWETestInput};

    #[test]
    fn test_case_builder() {
        let case = SWETestCase::new(
            "Test file read",
            SWETaskType::FileRead,
            SWETestInput::with_files("Read the README", vec!["README.md".to_string()]),
            SWETestExpectedOutput::any(),
        )
        .with_tag("file-ops");

        assert_eq!(case.name, "Test file read");
        assert_eq!(case.task_type, SWETaskType::FileRead);
        assert!(case.tags.contains(&"file-ops".to_string()));
    }

    #[test]
    fn test_swe_test_suite() {
        let mut suite = SWETestSuite::new("Basic Suite", "Basic SWE tests");

        suite.add_case(SWETestCase::new(
            "Read test",
            SWETaskType::FileRead,
            SWETestInput::from_description("Read a file"),
            SWETestExpectedOutput::any(),
        ));

        assert_eq!(suite.test_cases.len(), 1);
    }
}
