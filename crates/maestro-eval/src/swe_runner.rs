//! SWE Test Runner
//!
//! Executes SWE test cases using the SWEAgentExecutor and validates results.

use crate::swe::{SWESuiteReport, SWETaskType, SWETestCase, SWETestResult, SWETestSuite};
use chrono::Utc;
use maestro_swe::{SWEAgentAction, SWEAgentEvent, SWEAgentExecutor};
use std::path::Path;
use std::process::Command;
use std::time::Instant;
use tracing::{debug, info};
use uuid::Uuid;

// ---------------------------------------------------------------------------
// SWE Test Runner
// ---------------------------------------------------------------------------

/// Runner for SWE test cases.
pub struct SWETestRunner {
    executor: SWEAgentExecutor,
    #[allow(dead_code)]
    base_dir: Option<String>,
}

impl Default for SWETestRunner {
    fn default() -> Self {
        Self::new()
    }
}

impl SWETestRunner {
    /// Create a new SWE test runner.
    pub fn new() -> Self {
        Self {
            executor: SWEAgentExecutor::new(),
            base_dir: None,
        }
    }

    /// Create a runner with a base directory for tests.
    pub fn with_base_dir(dir: impl Into<String>) -> Self {
        Self {
            executor: SWEAgentExecutor::new(),
            base_dir: Some(dir.into()),
        }
    }

    /// Run a single test case.
    pub fn run_test(&self, test: &SWETestCase) -> SWETestResult {
        let start = Instant::now();
        let mut result = SWETestResult {
            test_case_id: test.id,
            test_case_name: test.name.clone(),
            passed: false,
            score: 0.0,
            duration_ms: 0,
            files_created: Vec::new(),
            files_modified: Vec::new(),
            command_outputs: Vec::new(),
            message: None,
            validation_details: Vec::new(),
            evaluated_at: Utc::now(),
        };

        // Run setup if provided
        if let Some(setup) = &test.setup {
            debug!("Running setup: {}", setup);
            if let Err(e) = self.run_command(setup) {
                result.message = Some(format!("Setup failed: {}", e));
                result.duration_ms = start.elapsed().as_millis() as u64;
                return result;
            }
        }

        // Execute the test based on task type
        let execution_result = self.execute_test(test, &mut result);

        // Validate results
        let validation_result = self.validate_results(test, &result);

        // Calculate score
        let score = self.calculate_score(test, &execution_result, &validation_result);
        result.score = score;
        result.passed = score >= 0.8;

        // Run cleanup if provided
        if let Some(cleanup) = &test.cleanup {
            debug!("Running cleanup: {}", cleanup);
            let _ = self.run_command(cleanup);
        }

        result.duration_ms = start.elapsed().as_millis() as u64;
        result
    }

    /// Execute the test based on its type.
    fn execute_test(&self, test: &SWETestCase, result: &mut SWETestResult) -> Vec<SWEAgentEvent> {
        let mut events = Vec::new();

        // Read files
        for file in &test.input.files_to_read {
            let action = SWEAgentAction::ReadFile(file.clone());
            let event = self.executor.execute(action);
            events.push(event.clone());

            if let SWEAgentEvent::FileRead(path, content) = event {
                debug!("Read file {}: {} bytes", path, content.len());
            }
        }

        // Run commands
        for cmd in &test.input.commands_to_run {
            let action = SWEAgentAction::ExecuteCommand(cmd.clone());
            let event = self.executor.execute(action);
            events.push(event.clone());

            if let SWEAgentEvent::CommandExecuted(cmd, output, exit_code) = event {
                debug!("Command '{}' exited with code {}", cmd, exit_code);
                result.command_outputs.push((cmd, output, exit_code));
            }
        }

        // Task-type specific execution
        match &test.task_type {
            SWETaskType::FileRead => {
                // Already handled above
            }
            SWETaskType::FileWrite => {
                // For file write tests, the task description should indicate what to write
                // We simulate by executing a write based on expected output
                for file in &test.expected.files_created {
                    let content = "# Generated by SWE agent\n".to_string();
                    let action = SWEAgentAction::WriteFile(file.clone(), content);
                    let event = self.executor.execute(action);
                    events.push(event.clone());

                    if let SWEAgentEvent::FileWritten(path) = event {
                        result.files_created.push(path);
                    }
                }
            }
            SWETaskType::CommandExecution => {
                // Already handled above
            }
            SWETaskType::CodeGeneration => {
                // For code generation, we would typically call an LLM here
                // For now, we just validate that expected files were created
                debug!("Code generation test: {}", test.name);
            }
            SWETaskType::BugFix => {
                // For bug fixes, we validate that the fix was applied correctly
                debug!("Bug fix test: {}", test.name);
            }
            SWETaskType::Refactoring => {
                // For refactoring, we validate that files were modified correctly
                debug!("Refactoring test: {}", test.name);
            }
            SWETaskType::MultiStep => {
                // Multi-step tests are handled by setup + commands
                debug!("Multi-step test: {}", test.name);
            }
        }

        events
    }

    /// Validate test results against expected output.
    fn validate_results(&self, test: &SWETestCase, result: &SWETestResult) -> ValidationResult {
        let mut details = Vec::new();
        let mut passed_checks = 0;
        let mut total_checks = 0;

        // Check files created
        for expected_file in &test.expected.files_created {
            total_checks += 1;
            if result.files_created.iter().any(|f| f == expected_file) {
                passed_checks += 1;
                details.push(format!("✓ File created: {}", expected_file));
            } else if Path::new(expected_file).exists() {
                passed_checks += 1;
                details.push(format!("✓ File exists: {}", expected_file));
            } else {
                details.push(format!("✗ File not created: {}", expected_file));
            }
        }

        // Check files modified
        for expected_file in &test.expected.files_modified {
            total_checks += 1;
            if result.files_modified.iter().any(|f| f == expected_file) {
                passed_checks += 1;
                details.push(format!("✓ File modified: {}", expected_file));
            } else {
                details.push(format!("✗ File not modified: {}", expected_file));
            }
        }

        // Check content patterns
        for (file, pattern) in &test.expected.content_patterns {
            total_checks += 1;
            if let Ok(content) = std::fs::read_to_string(file) {
                if content.contains(pattern) {
                    passed_checks += 1;
                    details.push(format!("✓ Pattern '{}' found in {}", pattern, file));
                } else {
                    details.push(format!("✗ Pattern '{}' not found in {}", pattern, file));
                }
            } else {
                details.push(format!("✗ Could not read file: {}", file));
            }
        }

        // Check command outputs
        for pattern in &test.expected.command_output_patterns {
            total_checks += 1;
            let found = result
                .command_outputs
                .iter()
                .any(|(_, output, _)| output.contains(pattern));
            if found {
                passed_checks += 1;
                details.push(format!("✓ Output contains: {}", pattern));
            } else {
                details.push(format!("✗ Output missing: {}", pattern));
            }
        }

        // Check compile if required
        if test.expected.should_compile {
            total_checks += 1;
            // Try to compile if there are Rust files
            if Path::new("Cargo.toml").exists() {
                let compile_result = Command::new("cargo").args(["check", "--quiet"]).output();
                match compile_result {
                    Ok(output) if output.status.success() => {
                        passed_checks += 1;
                        details.push("✓ Code compiles".to_string());
                    }
                    Ok(_) => {
                        details.push("✗ Code does not compile".to_string());
                    }
                    Err(e) => {
                        details.push(format!("✗ Compile check failed: {}", e));
                    }
                }
            } else {
                passed_checks += 1;
                details.push("⊘ No compilation needed".to_string());
            }
        }

        // Default: at least one check must exist
        if total_checks == 0 {
            total_checks = 1;
            passed_checks = 1;
            details.push("⊘ No validation checks defined".to_string());
        }

        ValidationResult {
            passed_checks,
            total_checks,
            details,
        }
    }

    /// Calculate the score for a test result.
    fn calculate_score(
        &self,
        _test: &SWETestCase,
        _execution: &[SWEAgentEvent],
        validation: &ValidationResult,
    ) -> f64 {
        if validation.total_checks == 0 {
            return 0.0;
        }
        validation.passed_checks as f64 / validation.total_checks as f64
    }

    /// Run a shell command.
    fn run_command(&self, cmd: &str) -> Result<(), String> {
        let output = Command::new("sh")
            .arg("-c")
            .arg(cmd)
            .output()
            .map_err(|e| e.to_string())?;

        if output.status.success() {
            Ok(())
        } else {
            Err(String::from_utf8_lossy(&output.stderr).to_string())
        }
    }

    /// Run an entire test suite.
    pub fn run_suite(&self, suite: &SWETestSuite) -> SWESuiteReport {
        let start = Instant::now();
        let run_id = Uuid::new_v4();

        info!(
            "Starting SWE eval run {} for suite '{}' v{}",
            run_id, suite.name, suite.version
        );

        let results: Vec<SWETestResult> =
            suite.test_cases.iter().map(|t| self.run_test(t)).collect();

        let total = results.len();
        let passed = results.iter().filter(|r| r.passed).count();
        let failed = total - passed;
        let pass_rate = if total > 0 {
            passed as f64 / total as f64
        } else {
            0.0
        };
        let avg_score = if total > 0 {
            results.iter().map(|r| r.score).sum::<f64>() / total as f64
        } else {
            0.0
        };

        info!(
            "Suite '{}': {}/{} passed ({:.1}%), avg score {:.3}",
            suite.name,
            passed,
            total,
            pass_rate * 100.0,
            avg_score
        );

        SWESuiteReport {
            suite_id: suite.id,
            suite_name: suite.name.clone(),
            run_id,
            results,
            total,
            passed,
            failed,
            pass_rate,
            avg_score,
            total_duration_ms: start.elapsed().as_millis() as u64,
            run_at: Utc::now(),
        }
    }
}

// ---------------------------------------------------------------------------
// Validation Result
// ---------------------------------------------------------------------------

struct ValidationResult {
    passed_checks: usize,
    total_checks: usize,
    #[allow(dead_code)]
    details: Vec<String>,
}

